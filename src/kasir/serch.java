    private void tblSerchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSerchMouseClicked
        // TODO add your handling code here:
        int row = tblSerch.getSelectedRow();
        if (row >= 0) {
            int modelRow = tblSerch.convertRowIndexToModel(row);

            String nama = tblSerch.getModel().getValueAt(modelRow, 0).toString();
            String hargaRp = tblSerch.getModel().getValueAt(modelRow, 2).toString();
            
            Transaksi.tNamaMenu.setText(nama);
            Transaksi.tHarga.setText(hargaRp);
            this.dispose();
        }

    }//GEN-LAST:event_tblSerchMouseClicked

