    void load_tabel_serch() {
        model = new DefaultTableModel();
        model.addColumn("Nama Menu");
        model.addColumn("Kategori");
        model.addColumn("Harga");

        String sql = "SELECT m.nama_menu, km.nama_kategori, m.harga " +
                     "FROM menu m " +
                     "JOIN kategori_menu km ON m.id_kategori = km.id_kategori";

        try {
            Connection conn = koneksi.konek();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                String nama = rs.getString("nama_menu");
                String kategori = rs.getString("nama_kategori");
                int hargaInt = rs.getInt("harga");

                String harga = formatRupiah(hargaInt); // format ke Rp

                Object[] baris = {nama, kategori, harga};
                model.addRow(baris);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal mengambil data menu!");
        }

        tblSerch.setModel(model);
        rowSorter = new TableRowSorter<>(model);
        tblSerch.setRowSorter(rowSorter);
    }
    

